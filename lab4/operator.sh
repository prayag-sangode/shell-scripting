#!/bin/bash

# Arithmetic Operators Script

# Addition
num1=14
num2=7
result_addition=$((num1 + num2))
echo "Addition: $num1 + $num2 = $result_addition"

# Subtraction
num1=10
num2=5
result_subtraction=$((num1 - num2))
echo "Subtraction: $num1 - $num2 = $result_subtraction"

# Multiplication
num1=10
num2=5
result_multiplication=$((num1 * num2))
echo "Multiplication: $num1 * $num2 = $result_multiplication"

# Division
num1=10
num2=5
result_division=$((num1 / num2))
echo "Division: $num1 / $num2 = $result_division"

# Modulus
num1=10
num2=3
result_modulus=$((num1 % num2))
echo "Modulus: $num1 % $num2 = $result_modulus"
